package com.library.management.model;

import java.util.Objects;

/**
 * Represents a physical address associated with a user in the Library Management System.
 * <p>
 * This class models typical address components such as street, city, pin code, state, and country.
 * It is used throughout the system wherever a user's address needs to be stored or displayed.
 *
 * <p>Example usage:
 * <pre>
 *     Address address = new Address("123 Main St", "New York", "10001", "NY", "USA");
 * </pre>
 *
 * @author Fardaws Jawad
 *
 * @see com.library.management.dao.AddressDAO
 * @see com.library.management.model.User
 */
public class Address {

    /** Unique identifier for the address (typically generated by the database). */
    private int addressId;

    /** Street portion of the address (e.g., house number, street name). */
    private String street;

    /** City where the address is located. */
    private String city;

    /** Postal or ZIP code for the address. */
    private String pinCode;

    /** State or province of the address. */
    private String state;

    /** Country of the address. */
    private String country;

    //-----------------------------------------------------------------------
    /**
     * Default no-argument constructor.
     * Useful for frameworks or libraries that require a no-arg constructor (e.g., JDBC, Hibernate).
     */
    public Address() {
    }

    /**
     * Constructs an Address object with all fields including the ID.
     * Useful when retrieving address data from the database.
     *
     * @param addressId the unique ID of the address
     * @param street    the street portion of the address
     * @param city      the city
     * @param pinCode   the postal code
     * @param state     the state
     * @param country   the country
     */
    public Address(int addressId, String street, String city, String pinCode, String state, String country) {
        this(street, city, pinCode, state, country);
        this.addressId = addressId;
    }


    /**
     * Constructs an Address object without the ID.
     * Useful when creating new address entries to be saved to the database.
     *
     * @param street   the street portion of the address
     * @param city     the city
     * @param pinCode  the postal code
     * @param state    the state
     * @param country  the country
     */
    public Address(String street, String city, String pinCode, String state, String country) {
        this.street = street;
        this.city = city;
        this.pinCode = pinCode;
        this.state = state;
        this.country = country;
    }

    //-----------------------------------------------------------------------
    /** @return the unique address ID */
    public int getAddressId() {
        return addressId;
    }

    /** @param addressId sets the unique ID of the address */
    public void setAddressId(int addressId) {
        this.addressId = addressId;
    }

    /** @return the street portion of the address */
    public String getStreet() {
        return street;
    }

    /** @param street sets the street portion of the address */
    public void setStreet(String street) {
        this.street = street;
    }

    /** @return the city of the address */
    public String getCity() {
        return city;
    }

    /** @param city sets the city of the address */
    public void setCity(String city) {
        this.city = city;
    }

    /** @return the postal (pin) code */
    public String getPinCode() {
        return pinCode;
    }

    /** @param pinCode sets the postal (pin) code */
    public void setPinCode(String pinCode) {
        this.pinCode = pinCode;
    }

    /** @return the state of the address */
    public String getState() {
        return state;
    }

    /** @param state sets the state of the address */
    public void setState(String state) {
        this.state = state;
    }

    /** @return the country of the address */
    public String getCountry() {
        return country;
    }

    /** @param country sets the country of the address */
    public void setCountry(String country) {
        this.country = country;
    }


    //-----------------------------------------------------------------------
    /**
     * Returns a string representation of the address for display or logging purposes.
     *
     * @return a formatted string of the address fields
     */
    @Override
    public String toString() {
        return "street: '" + street + '\'' +
                ", city: '" + city + '\'' +
                ", pinCode: '" + pinCode + '\'' +
                ", state: '" + state + '\'' +
                ", country: '" + country + '\'';
    }

    //-----------------------------------------------------------------------
    /**
     * Compares two Address objects for equality based on their field values (excluding addressId).
     *
     * @param object the object to compare with
     * @return true if all fields (except ID) match, false otherwise
     */
    @Override
    public boolean equals(Object object) {
        if (this == object) return true;
        if (object == null || getClass() != object.getClass()) return false;
        Address address = (Address) object;
        return Objects.equals(street, address.street) && Objects.equals(city, address.city) && Objects.equals(pinCode, address.pinCode) && Objects.equals(state, address.state) && Objects.equals(country, address.country);
    }

    //-----------------------------------------------------------------------
    /**
     * Generates a hash code based on address field values (excluding addressId).
     *
     * @return hash code for the address object
     */
    @Override
    public int hashCode() {
        return Objects.hash(street, city, pinCode, state, country);
    }
}
